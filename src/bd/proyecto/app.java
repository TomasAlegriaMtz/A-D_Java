    private void bBuscarIdCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarIdCursoActionPerformed
        // TODO add your handling code here:
        String consulta = "SELECT * FROM Curso"
                + " WHERE IdCurso"  
                + " = '" + txtIdCurso.getText() + "';";

        System.out.println(consulta);
        objConn.consult(consulta);
        
        int n = 0;
        int i, j;

        if (objConn.rs != null) {
            try {
                objConn.rs.last();
                n = objConn.rs.getRow();
                objConn.rs.first();
            } catch (Exception e) {
                lblCurso.setText("ID no encontrado");
            }

            String[][] datos = new String[n][10];
            for (i = 0; i < n; i++) {
                for (j = 0; j < 10; j++) {
                    try {
                        datos[i][j] = objConn.rs.getString(j + 1);
                    } catch (Exception e) {
                    }

                }
                try {
                    objConn.rs.next();
                } catch (Exception e) {
                }
            }

            if(n!=0){
                this.txtNombreCurso.setText(datos[0][1]);
            this.txtCupoCurso.setText(datos[0][2]);
            this.txtHorasCurso.setText(datos[0][3]);
            //this.txtRecolectado.setText(datos[0][4]);
            this.txtIdSedeCurso.setText(datos[0][5]);
            this.txtIdInstCurso.setText(datos[0][6]);
            
            
            String ano,mes,dia,fecha;
            fecha=datos[0][7];
            int l=0,m=0;
            
            while(fecha.charAt(l)!='-'){
                l++;
            }
            ano=fecha.substring(0,l);
            m=l;
            l++;
            while(fecha.charAt(l)!='-'){
                l++;
            }
            mes=fecha.substring(m+1,l);
            dia=fecha.substring(l+1,fecha.length());
            this.txtAno.setText(ano);
            this.txtMes.setText(mes);
            this.txtDia.setText(dia);
            }
               
        }
    }//GEN-LAST:event_bBuscarIdCursoActionPerformed

